PROGRAM TEST
  USE ISO_FORTRAN_ENV, ONLY: REAL64
  IMPLICIT NONE
  REAL(KIND=REAL64) :: A, C
  A = HUGE(0.0_REAL64) - EPSILON(0.0_REAL64)*HUGE(0.0_REAL64)
  PRINT *, 'A       :', A
  C = EPSILON(0.0_REAL64)
  PRINT *, 'C       :', C
  PRINT *, 'A+C     :', A+C
  PRINT *, 'A+C*A   :', A+C*A
  PRINT *, 'A*(C+1) :', A*(C+1.0_REAL64)

END PROGRAM TEST
